<h4>What is an operator profile?</h4>

<p>An operator profile is your name and callsign that you will use for the 
ROTA event.</p>

<p>If you're taking part in ROTA individually, you should create a 
<strong>personal</strong> profile. However, if you're a club, you should 
create a <strong>club</strong> one.</p>

<p><span class="label notice">Note</span> You can add more than one profile to 
your account; for example, if you're taking part on your own but are also 
responsible for registering a club, or if you use a different callsign a 
following year.</p>